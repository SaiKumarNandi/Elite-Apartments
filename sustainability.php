                Choose sustainable ingredients: Buy local and organic produce when possible, and choose sustainably sourced seafood and meat
                products. Avoid processed and packaged foods, and opt for whole foods that require less energy and resources to produce.
            </p>
            <p>
                Use energy-efficient appliances: Upgrade to Energy Star-certified appliances that use less electricity and water. When
                cooking, use lids to trap heat and reduce cooking time, and use the microwave or toaster oven for small meals instead of
                the oven.
            </p>
            <p>
                Use a dishwasher rather than cleaning the dishes by hand, and only run it when it is completely full to conserve water.
                To save water, dispose of food waste in a compost container rather than with running water and rectify any leaks.
            </p>
            <p>
                Avoid using single-use plastics like straws, bags, and utensils to reduce plastic waste. Use reusable bags and containers
                to store food, and use cloth napkins over disposable ones.
            </p>
            <p>
                You may lessen your influence on the environment and encourage sustainability for future generations by making minor
                changes in the kitchen.
            </p>

